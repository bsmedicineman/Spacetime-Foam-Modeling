"""
craft_trajectory_plotter.py

Create an animated 3‑D trajectory for one or more spacecraft (or probes)
that runs together with the foam‑distortion field generated by
solar_foam_animation.py.  The output is a series of VTK Structured‑Grid
files (the foam field) plus a matching JSON side‑car that contains the
craft state (position, velocity, speed, orientation, colour).

All physics that is needed to *place* the craft comes from the
Master‑Equation Reference:

* §8.5 / §8.6 – travel time, proper‑time integral → we interpolate the
  supplied ephemeris.
* §8.1 – effective foam‑gradient force (exported in the foam VTK, not
  used for the baseline trajectory, but available for later navigation
  models.
* §5.17 – foam‑field energy (phi_dist) is already stored in the foam
  VTK files.
* §9.1 – gravity‑foam coupling (α_g) – not needed for a pure
  ephemeris‑driven plot, but you could later replace the static
  positions with a foam‑driven navigation solution.

Author:  BSMEDICINEMAN (06.13.2026)   License: MIT
"""

