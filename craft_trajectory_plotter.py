# ----------------------------------------------------------------------
# 1️⃣  CONSTANTS
# ----------------------------------------------------------------------
AU_M = 1.495978707e11          # metres
SECONDS_PER_DAY = 86400.0

# ----------------------------------------------------------------------
# 2️⃣  DATA structures for a single craft
# ----------------------------------------------------------------------
class Craft:
    """
    Holds the raw ephemeris (time‑stamped positions, optional velocities
    and attitude) and provides interpolation utilities.
    """
    def __init__(self,
                 name: str,
                 colour: Tuple[int, int, int],
                 df: pd.DataFrame):
        """
        df must contain at least the columns:
            time_iso, x, y, z   (all in AU)
        Optional columns:
            vx, vy, vz (AU/day), roll, pitch, yaw (deg)
        """
        self.name = name
        self.colour = colour

        # Ensure the time column is a datetime index
        df = df.copy()
        df["time"] = pd.to_datetime(df["time_iso"], utc=True)
        df.set_index("time", inplace=True)
        self.df = df.sort_index()

        # If velocity columns are missing, compute them by central diff
        if not {"vx", "vy", "vz"}.issubset(df.columns):
            self._compute_velocities()

    def _compute_velocities(self):
        """Finite‑difference velocity in AU day⁻¹."""
        pos = self.df[["x", "y", "z"]].values
        times = self.df.index.astype("int64") / 1e9  # seconds since epoch
        dt_days = np.gradient(times) / SECONDS_PER_DAY
        vel = np.gradient(pos, axis=0) / dt_days[:, None]   # AU/day
        self.df["vx"] = vel[:, 0]
        self.df["vy"] = vel[:, 1]
        self.df["vz"] = vel[:, 2]

    # ------------------------------------------------------------------
    # 2️⃣.1  Interpolation interface
    # ------------------------------------------------------------------
    def state_at(self, t: datetime) -> Dict:
        """
        Return a dictionary with interpolated state at absolute time `t`.
        Keys: pos (np.array AU), vel (np.array AU/day),
              speed_kms (float km/s), attitude (roll,pitch,yaw) in deg.
        """
        # If the exact timestamp exists, just return it
        if t in self.df.index:
            row = self.df.loc[t]
        else:
            # pandas' `reindex` with method='nearest' would snap – we need
            # true interpolation.
            row = self._interpolate(t)

        pos = np.array([row["x"], row["y"], row["z"]], dtype=np.float64)
        vel = np.array([row["vx"], row["vy"], row["vz"]], dtype=np.float64)

        # Speed in km/s (AU/day → km/s)
        speed_kms = np.linalg.norm(vel) * AU_M / SECONDS_PER_DAY / 1e3

        # Attitude – default to zeros if not present
        attitude = (
            float(row.get("roll", 0.0)),
            float(row.get("pitch", 0.0)),
            float(row.get("yaw", 0.0)),
        )

        return {
            "pos_au": pos.tolist(),
            "vel_au_per_day": vel.tolist(),
            "speed_kms": speed_kms,
            "attitude_deg": attitude,
            "name": self.name,
            "color_rgb": self.colour,
        }

    def _interpolate(self, t: datetime) -> pd.Series:
        """
        Linear (or spline) interpolation between the two nearest rows.
        """
        times = self.df.index
        if t < times[0] or t > times[-1]:
            raise ValueError(f"Requested time {t.isoformat()} is outside the "
                             f"ephemeris range for craft {self.name}")

        # Find surrounding timestamps
        lower = times[times <= t][-1]
        upper = times[times >= t][0]

        if lower == upper:
            return self.df.loc[lower]

        # Linear blend factor
        f = (t - lower).total_seconds() / (upper - lower).total_seconds()
        lower_row = self.df.loc[lower]
        upper_row = self.df.loc[upper]

        # Interpolate each numeric column
        interp = lower_row.copy()
        for col in lower_row.index:
            if col == "time_iso":
                continue
            v_low = lower_row[col]
            v_up  = upper_row[col]
            interp[col] = (1 - f) * v_low + f * v_up
        return interp


# ----------------------------------------------------------------------
# 3️⃣  READ PROBE / CRAFT FILES
# ----------------------------------------------------------------------
def read_probe_file(path: Path) -> Craft:
    """
    Accept CSV or JSON.  The file must contain the columns described in
    the “Data format” section.  If a `name` column is present it will be
    used, otherwise the stem of the filename is taken.
    """
    if not path.exists():
        raise FileNotFoundError(str(path))

    if path.suffix.lower() in {".csv", ".txt"}:
        df = pd.read_csv(path)
    elif path.suffix.lower() in {".json", ".js"}:
        df = pd.read_json(path)
    else:
        raise ValueError(f"Unsupported file type: {path}")

    # Normalise column names (strip spaces, lower‑case)
    df.columns = [c.strip().lower() for c in df.columns]

    # Required columns
    required = {"time_iso", "x", "y", "z"}
    if not required.issubset(df.columns):
        missing = required - set(df.columns)
        raise ValueError(f"Probe file {path} missing columns: {missing}")

    # Optional colour columns – default to white
    if {"color_r", "color_g", "color_b"}.issubset(df.columns):
        colour = (int(df["color_r"].iloc[0]),
                  int(df["color_g"].iloc[0]),
                  int(df["color_b"].iloc[0]))
    else:
        # fallback: assign a deterministic colour based on the file name
        # (simple hash → hue)
        hue = (hash(path.stem) % 360) / 360.0
        colour = hsv_to_rgb(hue, 0.7, 0.9)   # returns 0‑255 triplet

    # Name
    if "name" in df.columns:
        name = str(df["name"].iloc[0])
    else:
        name = path.stem

    return Craft(name=name, colour=colour, df=df)


def hsv_to_rgb(h, s, v):
    """Convert HSV (0‑1) to 0‑255 RGB tuple."""
    import colorsys
    r, g, b = colorsys.hsv_to_rgb(h, s, v)
    return (int(r * 255), int(g * 255), int(b * 255))


# ----------------------------------------------------------------------
# 4️⃣  LOAD THE BASELINE FOAM GRID (only needed for geometry)
# ----------------------------------------------------------------------
def load_baseline_grid():
    """
    Import the FoamMap class from baseline_foam_map.py and return the
    coordinate arrays plus spacing/origin.  The geometry is needed only
    so that the visualiser can align the craft JSON with the VTK grid.
    """
    import baseline_foam_map as bfm
    foam = bfm.FoamMap()
    return (foam.x_grid, foam.y_grid, foam.z_grid), foam.spacing_m, -foam.r_max_m


# ----------------------------------------------------------------------
# 5️⃣  MAIN DRIVER – generate frames
# ----------------------------------------------------------------------
def generate_frames(start: datetime,
                    end: datetime,
                    dt_seconds: float,
                    craft_list: List[Craft],
                    foam_dir: Path,
                    out_dir: Path):
    """
    For each simulation step:
      1. Load the corresponding foam VTK file (if it exists).  
      2. Interpolate every craft to the same simulation time.  
      3. Write a JSON side‑car named `craft_XXXXX.json` that contains
         the state of all crafts for that frame.
    The VTK files themselves are *not* rewritten – they are produced
    by solar_foam_animation.py and are assumed to already be in `foam_dir`.
    """
    # Ensure output folder exists
    out_dir.mkdir(parents=True, exist_ok=True)

    # Load the static geometry (so we can sanity‑check the grid size)
    (X, Y, Z), spacing_m, origin_m = load_baseline_grid()
    nx, ny, nz = X.shape
    print(f"[Info] Baseline grid: {nx}×{ny}×{nz} cells, spacing = {spacing_m:.2e} m")

    # Number of frames
    total_seconds = (end - start).total_seconds()
    n_frames = int(np.ceil(total_seconds / dt_seconds))

    print(f"[Info] Generating {n_frames} frames from {start.isoformat()} "
          f"to {end.isoformat()} (Δt = {dt_seconds/3600:.2f} h)")

    for idx in tqdm(range(n_frames), desc="Frames"):
        current_time = start + timedelta(seconds=idx * dt_seconds)

        # --------------------------------------------------------------
        # 5.1  Check that the foam VTK for this frame exists
        # --------------------------------------------------------------
        foam_file = foam_dir / f"foam_{idx:05d}.vts"
        if not foam_file.is_file():
            # If the file is missing we could recompute it on‑the‑fly,
            # but for now we stop with a clear message.
            raise FileNotFoundError(
                f"Missing foam frame {foam_file}.  Run solar_foam_animation.py "
                f"with the same start‑time / dt to generate it."
            )

        # --------------------------------------------------------------
        # 5.2  Interpolate each craft to the current simulation time
        # --------------------------------------------------------------
        craft_states = []
        for craft in craft_list:
            try:
                state = craft.state_at(current_time)
                craft_states.append(state)
            except ValueError as e:
                # If the craft is outside its ephemeris range we simply
                # skip it for this frame (e.g. a probe that has not yet
                # launched).  The viewer will just not see it.
                continue

        # --------------------------------------------------------------
        # 5.3  Write the JSON side‑car
        # --------------------------------------------------------------
        json_path = out_dir / f"craft_{idx:05d}.json"
        with open(json_path, "w") as f:
            json.dump(craft_states, f, indent=2)

        # --------------------------------------------------------------
        # 5.4  Copy the foam VTK file into the output folder (optional)
        # --------------------------------------------------------------
        # Some visualisers expect the VTK and JSON to be in the same folder.
        # We simply create a hard‑link (or copy if the OS does not support it).
        target_vtk = out_dir / foam_file.name
        try:
            os.link(foam_file, target_vtk)   # fast hard‑link
        except OSError:
            # Fall back to a regular copy
            import shutil
            shutil.copy2(foam_file, target_vtk)

    print("\n[Done] All frames written to:", out_dir.resolve())
    print("Load the VTK files in your favourite 3‑D viewer and pair each "
          "with its matching `craft_XXXXX.json` side‑car.")


# ----------------------------------------------------------------------
# 6️⃣  COMMAND‑LINE INTERFACE
# ----------------------------------------------------------------------
def parse_args():
    parser = argparse.ArgumentParser(
        description="Generate a 3‑D animation of spacecraft trajectories "
                    "that runs together with the foam‑distortion field "
                    "produced by solar_foam_animation.py."
    )
    parser.add_argument("--start", required=True,
                        help="ISO‑8601 start time (e.g. 2025-01-01T00:00:00)")
    parser.add_argument("--end", required=True,
                        help="ISO‑8601 end time (e.g. 2025-12-31T00:00:00)")
    parser.add_argument("--dt-hours", type=float, default=1.0,
                        help="Time step in hours (default 1 h)")
    parser.add_argument("--probe-file", action="append", required=True,
                        help="Path to a CSV/JSON file containing probe ephemeris. "
                             "You may repeat this argument for multiple crafts.")
    parser.add_argument("--foam-dir", type=Path, default=Path("./foam_frames"),
                        help="Folder where solar_foam_animation VTK files live.")
    parser.add_argument("--output", type=Path, default=Path("./animation_frames"),
                        help="Folder where the combined animation will be written.")
    parser.add_argument("--interp", choices=["linear", "spline"], default="linear",
                        help="Interpolation method for craft ephemerides (default linear).")
    return parser.parse_args()


# ----------------------------------------------------------------------
# 7️⃣  ENTRY POINT
# ----------------------------------------------------------------------
if __name__ == "__main__":
    args = parse_args()

    # ------------------------------------------------------------------
    # 7.1  Parse timestamps
    # ------------------------------------------------------------------
    start_dt = datetime.fromisoformat(args.start.replace("Z", "+00:00"))
    end_dt   = datetime.fromisoformat(args.end.replace("Z", "+00:00"))
    if end_dt <= start_dt:
        sys.exit("[Error] End time must be after start time.")

    dt_seconds = args.dt_hours * 3600.0

    # ------------------------------------------------------------------
    # 7.2  Load every probe / craft file
    # ------------------------------------------------------------------
    crafts: List[Craft] = []
    for fp in args.probe_file:
        path = Path(fp)
        craft = read_probe_file(path)
        crafts.append(craft)
        print(f"[Info] Loaded craft \"{craft.name}\" from {path}")

    # ------------------------------------------------------------------
    # 7.3  Generate the animation frames
    # ------------------------------------------------------------------
    generate_frames(start=start_dt,
                   end=end_dt,
                   dt_seconds=dt_seconds,
                   craft_list=crafts,
                   foam_dir=args.foam_dir,
                   out_dir=args.output)
